/**
 * Customer Class.
 * Basic class that holds the arrival time of each customer.
 *
 * @author Jackson Campbell
 * @version 1.0
 */
public class Customer {

    private int arrivalTime;

    /**
     * Instantiates a new Customer.
     * Sets the customer's arrival time.
     * @param arrivalTime the arrival time of the given customer.
     */
    public Customer(int arrivalTime) {
        this.arrivalTime = arrivalTime;
    }

    /**
     * getArrivalTime integer.
     * Grabs the arrival time of a customer.
     * @return the arrival time of the given customer.
     */
    public int getArrivalTime() {
        return arrivalTime;
    }
























}
